                <h4>Bab 1: Kisah Terakbar yang Pernah Disampaikan</h4>
                <h6>Di bab pertama, Tyson mengajak kita untuk memahami alam semesta sejak momen Big Bang hingga sekarang. Dia menjelaskan bahwa semua materi dan energi yang ada di alam semesta saat ini berasal dari sebuah titik tunggal yang sangat padat dan panas sekitar 13,8 miliar tahun yang lalu. Dari ekspansi ini, terbentuklah partikel-partikel dasar yang kemudian membentuk atom, bintang, dan galaksi. Tyson menekankan pentingnya pemahaman ini dalam astrofisika untuk melacak asal-usul dan evolusi alam semesta.</h6>
                <br>
                <h4>Eksplorasi Astrofisika</h4>
                <h6>Astrofisika menggunakan berbagai alat dan teknik untuk mengamati dan mempelajari alam semesta. Teleskop, baik yang berbasis di Bumi maupun di luar angkasa, memungkinkan kita untuk mengamati objek-objek jauh dengan berbagai panjang gelombang cahaya. Selain itu, detektor partikel dan observatorium gelombang gravitasi membantu ilmuwan mempelajari fenomena eksotis seperti lubang hitam dan gelombang gravitasi yang dihasilkan oleh tabrakan objek masif.</h6>
                <br>
                <h4>Pentingnya Astrofisika</h4>
                <h6>Astrofisika membantu kita memahami hukum-hukum dasar yang mengatur alam semesta dan memberi kita wawasan tentang tempat kita di dalamnya. Penemuan-penemuan astrofisika telah mengubah pandangan kita tentang kosmos dan membuka kemungkinan-kemungkinan baru untuk eksplorasi ruang angkasa. Misalnya, pemahaman tentang energi gelap dan materi gelap telah memunculkan pertanyaan baru tentang komposisi dan nasib akhir alam semesta.</h6>
                <br>
                <h4>Bab 9: Cahaya Tak Tampak</h4>
                <h6>Di bab ini, Tyson membahas tentang fenomena-fenomena yang tidak dapat kita lihat dengan mata telanjang, seperti materi gelap dan energi gelap yang membentuk sebagian besar alam semesta. Meski tak terlihat, dampak mereka terhadap struktur dan dinamika kosmos sangat signifikan. Tyson menjelaskan bagaimana astrofisikawan menggunakan berbagai metode tidak langsung untuk mempelajari dan memahami entitas misterius ini.</h6>
                <br>
                <h4>Masa Depan Astrofisika</h4>
                <h6>Masa depan astrofisika sangat menjanjikan dengan berbagai misi dan observatorium baru yang akan memberikan wawasan lebih dalam tentang alam semesta. Proyek seperti James Webb Space Telescope, observatorium gelombang gravitasi seperti LIGO dan Virgo, serta misi ke planet dan bulan dalam tata surya kita, akan membantu menjawab pertanyaan-pertanyaan besar yang masih ada. Neil deGrasse Tyson mengajak kita untuk terus mencari jawaban dan tidak pernah berhenti bertanya tentang kosmos.</h6>
