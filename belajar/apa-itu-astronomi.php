                <h4>Bab 1: Tepi Lautan Kosmik</h4>
                <h6>Di bab pertama "Kosmos", Sagan membawa kita dalam perjalanan melintasi alam semesta, dimulai dari planet kita sendiri, Bumi, dan menjelajahi berbagai bintang dan galaksi di luar sana. Dia menjelaskan bahwa meskipun kita tampak kecil dan tidak berarti di alam semesta yang luas, kita memiliki kemampuan untuk memahami dan menjelajahi kosmos. Sagan menyatakan, "We are a way for the cosmos to know itself," menyoroti bahwa melalui astronomi, kita dapat memahami lebih banyak tentang asal-usul dan nasib kita.</h6>
                <br>
                <h4>Pentingnya Astronomi</h4>
                <h6>Astronomi tidak hanya memberi kita wawasan tentang alam semesta, tetapi juga membantu kita memahami tempat kita di dalamnya. Dengan mempelajari bintang dan galaksi yang jauh, kita dapat memahami proses yang membentuk tata surya kita sendiri dan bahkan kehidupan di Bumi. Selain itu, penemuan-penemuan astronomi telah membawa berbagai inovasi teknologi yang kita gunakan sehari-hari, seperti GPS dan telekomunikasi satelit.</h6>
                <br>
                <h4>Masa Depan Astronomi</h4>
