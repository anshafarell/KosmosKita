                <h4>Definisi</h4>
                <h6>Astronomi (bahasa Yunani: ἀστρονομία, translit. astronomía, dari ástron 'bintang' dan nómos 'hukum'), juga disebut ilmu bintang atau ilmu falak, adalah ilmu alam yang mempelajari benda langit dan fenomena alam yang terjadi di luar Bumi, termasuk fenomena di atmosfer atas Bumi yang berasal dari luar angkasa seperti meteor dan aurora. ("Astronomy". A Dictionary of Astronomy (edisi ke-3). Oxford University Press. 2018.)</h6>
                <h6>Astronomi adalah ilmu yang mempelajari benda-benda langit seperti bintang, planet, komet, dan galaksi, serta fenomena yang terjadi di luar atmosfer Bumi, seperti radiasi latar belakang kosmik. Ilmu ini mencoba menjawab pertanyaan-pertanyaan mendasar tentang alam semesta: Bagaimana ia terbentuk? Bagaimana ia berkembang? Dan apa saja hukum-hukum fisika yang mengaturnya?</h6>
                <h6>Dalam bukunya "Sejarah Singkat Waktu", Stephen Hawking menjelaskan bagaimana astronomi telah berkembang dari pengamatan kuno hingga teori-teori modern tentang alam semesta. Hawking menggambarkan bagaimana penemuan-penemuan astronomi telah mengubah pemahaman kita tentang tempat kita di alam semesta, mulai dari model geosentris hingga heliosentris dan akhirnya model kosmologis modern.</h6>
