                <h4>Sejarah Astronomi</h4>
                <h6>Sejak zaman kuno, manusia telah mengamati langit dan bertanya-tanya tentang alam semesta. Peradaban awal seperti Babilonia, Yunani, Cina, dan Maya, mengembangkan sistem astronomi yang kompleks untuk meramalkan pergerakan benda-benda langit dan membuat kalender. Dengan penemuan teleskop oleh Galileo Galilei pada abad ke-17, astronomi mulai berkembang menjadi ilmu yang lebih eksperimental dan observasional.</h6>
                <br>
                <h4>Carl Sagan dan "Kosmos"</h4>
                <h6>Dalam bukunya "Kosmos", Carl Sagan menjelaskan astronomi dengan cara yang mudah dipahami oleh semua orang. Dia mengajak kita untuk melihat alam semesta sebagai rumah kita dan memahami bahwa kita adalah bagian kecil dari kosmos yang luas. Sagan menggunakan contoh-contoh konkret untuk menjelaskan konsep-konsep astronomi yang kompleks, seperti evolusi bintang, pembentukan galaksi, dan kemungkinan adanya kehidupan di planet lain.</h6>
