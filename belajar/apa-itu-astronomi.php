                <h1>Astrofisika</h1>
                <hr>
                <h4>Apa itu Astrofisika?</h4>
                <h6>Astrofisika adalah cabang ilmu astronomi yang berfokus pada sifat fisik benda-benda langit dan fenomena alam semesta. Ia menggabungkan prinsip-prinsip fisika dan kimia untuk memahami bagaimana bintang, planet, galaksi, dan kosmos secara keseluruhan terbentuk, berkembang, dan berinteraksi. Astrofisika mencoba menjawab pertanyaan-pertanyaan besar seperti asal-usul alam semesta, struktur ruang dan waktu, serta sifat dasar materi dan energi.</h6>
                <br>
                <h4>Dasar-Dasar Astrofisika</h4>
                <h6>Neil deGrasse Tyson dalam bukunya "Astrofisika untuk Orang Sibuk" menjelaskan konsep-konsep utama astrofisika dengan cara yang ringkas dan mudah dipahami. Buku ini mengajak pembaca untuk merenungkan pertanyaan-pertanyaan mendasar tentang alam semesta dan memberikan wawasan tentang bagaimana ilmuwan menggunakan hukum-hukum fisika untuk mempelajari fenomena kosmik. Misalnya, Tyson membahas tentang gravitasi, spektrum elektromagnetik, dan mekanika kuantum dalam konteks kosmologis.</h6>
